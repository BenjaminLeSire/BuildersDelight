    private String toReadable(String registryName) {

        String nameWithoutNumber = registryName.replaceAll("_\\d+$", "");
        String[] parts = nameWithoutNumber.split("_");
        StringBuilder readableNameBuilder = new StringBuilder();

        for (String part : parts) {
            readableNameBuilder.append(part.substring(0, 1).toUpperCase()).append(part.substring(1)).append(" ");
        }

        return readableNameBuilder.toString().trim();
    }
